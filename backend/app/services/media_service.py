async def update_image_prompts(
    db: AsyncSession,
    skeleton_id: UUID,
    prompts_update: List[dict],
    deleted_prompt_indices: List[int]
) -> dict:
    """
    Update prompts in an image prompt skeleton (modify, add, delete).

    Args:
        db: Database session
        skeleton_id: Skeleton UUID
        prompts_update: List of prompts to update or add (with prompt_order)
        deleted_prompt_indices: List of prompt_order indices to delete

    Returns:
        dict: Updated skeleton

    Raises:
        ValueError: If skeleton not found or already completed
    """
    result = await db.execute(
        select(ImagePromptSkeletonDB).where(
            ImagePromptSkeletonDB.id == str(skeleton_id)
        )
    )
    skeleton = result.scalar_one_or_none()

    if not skeleton:
        raise ValueError(f"Image prompt skeleton not found: {skeleton_id}")

    if skeleton.status == ImagePromptSkeletonStatus.COMPLETED.value:
        raise ValueError("Cannot update skeleton after completion")

    # The frontend sends the COMPLETE list of prompts (not just changes)
    # So we replace the entire prompts_json with the new list
    # deleted_prompt_indices tells us which ones to explicitly exclude

    # Convert deleted indices to a set for fast lookup
    deleted_indices_set = set(deleted_prompt_indices)

    # Build the new prompts list
    updated_prompts = []
    for prompt_update in prompts_update:
        # Convert Pydantic model to dict
        if hasattr(prompt_update, 'model_dump'):
            prompt_dict = prompt_update.model_dump()
        elif hasattr(prompt_update, 'dict'):
            prompt_dict = prompt_update.dict()
        else:
            prompt_dict = prompt_update

        prompt_order = prompt_dict.get("prompt_order")

        # Only include if not in deleted list
        if prompt_order is not None and prompt_order not in deleted_indices_set:
            updated_prompts.append(prompt_dict)

    # Sort by prompt_order
    updated_prompts = sorted(updated_prompts, key=lambda p: p.get("prompt_order", 0))

    skeleton.prompts_json = updated_prompts
    skeleton.status = ImagePromptSkeletonStatus.EDITING.value
    skeleton.updated_at = datetime.now(timezone.utc)

    await db.flush()

    logger.info(
        f"Updated skeleton {skeleton_id}: {len(prompts_update)} prompts processed, "
        f"{len(deleted_prompt_indices)} deleted"
    )

    # Expire the skeleton object to force a fresh load from database
    # This ensures we get the latest prompts_json after updates
    db.expire(skeleton)

    return await get_image_prompt_skeleton(db, skeleton_id)


async def approve_image_prompt_skeleton(db: AsyncSession, skeleton_id: UUID) -> dict:
    """
    Approve an image prompt skeleton for generation.

    Args:
        db: Database session
        skeleton_id: Skeleton UUID

    Returns:
        dict: Approved skeleton

    Raises:
        ValueError: If skeleton not found
    """
    # CRITICAL: Expire all objects to prevent loading stale cached skeleton
    # This ensures we get the latest prompts_json from the database after any edits
    db.expire_all()

    result = await db.execute(
        select(ImagePromptSkeletonDB).where(
            ImagePromptSkeletonDB.id == str(skeleton_id)
        )
    )
    skeleton = result.scalar_one_or_none()

    if not skeleton:
        raise ValueError(f"Image prompt skeleton not found: {skeleton_id}")

    skeleton.status = ImagePromptSkeletonStatus.APPROVED.value
    skeleton.approved_at = datetime.now(timezone.utc)
    skeleton.updated_at = datetime.now(timezone.utc)

    await db.flush()

    logger.info(f"Approved image prompt skeleton {skeleton_id}")

    # Reload from database to get fresh state
    return await get_image_prompt_skeleton(db, skeleton_id)


