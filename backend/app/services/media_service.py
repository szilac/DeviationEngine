async def create_media(
    db: AsyncSession,
    generation_id: UUID,
    media_type: str,
    media_url: str,
    media_order: int,
    prompt_text: Optional[str] = None,
    media_local_path: Optional[str] = None,
    event_year: Optional[int] = None,
    title: Optional[str] = None,
    description: Optional[str] = None,
    is_user_added: bool = False,
    model_provider: Optional[str] = None,
    model_name: Optional[str] = None,
) -> dict:
    """
    Create a media record (image, audio, or video).

    Args:
        db: Database session
        generation_id: Generation UUID
        media_type: Type of media (image, audio, video)
        media_url: URL to media file
        media_order: Display order within generation
        prompt_text: AI prompt used for generation
        media_local_path: Local file path (optional)
        event_year: Associated year in timeline
        title: User-friendly title
        description: Caption or description
        is_user_added: True if manually added by user
        model_provider: LLM provider that generated prompt
        model_name: Model that generated prompt

    Returns:
        dict: Created media record
    """
    media_id = str(uuid4())

    db_media = MediaDB(
        id=media_id,
        generation_id=str(generation_id),
        media_type=media_type,
        media_order=media_order,
        prompt_text=prompt_text,
        media_url=media_url,
        media_local_path=media_local_path,
        event_year=event_year,
        title=title,
        description=description,
        is_user_added=1 if is_user_added else 0,
        is_user_modified=0,
        model_provider=model_provider,
        model_name=model_name,
        created_at=datetime.now(timezone.utc),
    )

    db.add(db_media)
    await db.flush()

    logger.info(
        f"Created {media_type} media {media_id} for generation {generation_id} "
        f"(order: {media_order})"
    )

    return await get_media(db, UUID(media_id))


async def get_media(db: AsyncSession, media_id: UUID) -> Optional[dict]:
    """
    Get a media record by ID.

    Args:
        db: Database session
        media_id: Media UUID

    Returns:
        Optional[dict]: Media if found, None otherwise
    """
    from sqlalchemy.orm import selectinload

    result = await db.execute(
        select(MediaDB)
        .options(selectinload(MediaDB.generation))
        .where(MediaDB.id == str(media_id))
    )
    media = result.scalar_one_or_none()

    if not media:
        logger.warning(f"Media not found: {media_id}")
        return None

    # Get timeline_id from generation
    timeline_id = None
    if media.generation:
        timeline_id = UUID(media.generation.timeline_id)

    return {
        "id": UUID(media.id),
        "timeline_id": timeline_id,
        "generation_id": UUID(media.generation_id),
        "media_type": media.media_type,
        "media_order": media.media_order,
        "prompt_text": media.prompt_text,
        "media_url": media.media_url,
        "image_url": media.media_url,  # Alias for backward compatibility
        "media_local_path": media.media_local_path,
        "event_year": media.event_year,
        "title": media.title,
        "description": media.description,
        "is_user_added": bool(media.is_user_added),
        "is_user_modified": bool(media.is_user_modified),
        "model_provider": media.model_provider,
        "model_name": media.model_name,
        "created_at": media.created_at,
    }


async def get_media_by_generation(
    db: AsyncSession, generation_id: UUID, media_type: Optional[str] = None
) -> List[dict]:
    """
    Get all media for a generation, optionally filtered by type.

    Args:
        db: Database session
        generation_id: Generation UUID
        media_type: Optional media type filter (image, audio, video)

    Returns:
        List[dict]: List of media records ordered by media_order
    """
    query = select(MediaDB).where(MediaDB.generation_id == str(generation_id))

    if media_type:
        query = query.where(MediaDB.media_type == media_type)

    query = query.order_by(MediaDB.media_order)

    result = await db.execute(query)
    media_list = result.scalars().all()

    responses = []
    for media in media_list:
        media_data = await get_media(db, UUID(media.id))
        if media_data:
            responses.append(media_data)

    logger.info(
        f"Retrieved {len(responses)} media items for generation {generation_id}"
        + (f" (type: {media_type})" if media_type else "")
    )
    return responses


async def get_media_by_timeline(
    db: AsyncSession, timeline_id: UUID, media_type: Optional[str] = None
) -> List[dict]:
    """
    Get all media for a timeline across all generations.

    Args:
        db: Database session
        timeline_id: Timeline UUID
        media_type: Optional media type filter (image, audio, video)

    Returns:
        List[dict]: List of media records
    """
    # Get all generations for timeline
    gen_result = await db.execute(
        select(GenerationDB).where(GenerationDB.timeline_id == str(timeline_id))
    )
    generations = gen_result.scalars().all()

    all_media = []
    for generation in generations:
        media_list = await get_media_by_generation(
            db, UUID(generation.id), media_type
        )
        all_media.extend(media_list)

    logger.info(
        f"Retrieved {len(all_media)} media items for timeline {timeline_id}"
        + (f" (type: {media_type})" if media_type else "")
    )
    return all_media


async def delete_media(db: AsyncSession, media_id: UUID) -> bool:
    """
    Delete a media record.

    Args:
        db: Database session
        media_id: Media UUID

    Returns:
        bool: True if deleted, False if not found
    """
    result = await db.execute(select(MediaDB).where(MediaDB.id == str(media_id)))
    media = result.scalar_one_or_none()

    if not media:
        logger.warning(f"Cannot delete - media not found: {media_id}")
        return False

    media_type = media.media_type
    generation_id = media.generation_id

    await db.delete(media)
    await db.flush()

    logger.info(
        f"Deleted {media_type} media {media_id} from generation {generation_id}"
    )
    return True
