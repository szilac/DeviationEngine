async def create_image_prompt_skeleton(
    db: AsyncSession,
    timeline_id: UUID,
    generation_id: Optional[UUID],
    num_images: int,
    focus_areas: Optional[List[str]],
    prompts: List[dict],
    model_provider: Optional[str] = None,
    model_name: Optional[str] = None,
) -> dict:
    """
    Create an image prompt skeleton from AI agent output.

    Args:
        db: Database session
        timeline_id: UUID of the timeline
        generation_id: Optional UUID of specific generation
        num_images: Number of images to generate
        focus_areas: Optional focus areas for image generation
        prompts: List of image prompts generated by illustrator agent
        model_provider: LLM provider used
        model_name: Specific model used

    Returns:
        dict: Created skeleton with prompts
    """
    skeleton_id = str(uuid4())

    # Get timeline to extract deviation date for year validation
    result = await db.execute(
        select(TimelineDB).where(TimelineDB.id == str(timeline_id))
    )
    db_timeline = result.scalar_one_or_none()

    if db_timeline:
        deviation_year = int(db_timeline.root_deviation_date.split("-")[0])

        # Validate and fix event_year values in prompts
        # Convert absolute years to relative years if LLM made a mistake
        for prompt in prompts:
            event_year = prompt.get("event_year")
            if event_year is not None and event_year > 100:
                # Looks like an absolute year, convert to relative
                relative_year = event_year - deviation_year
                logger.warning(
                    f"Converting absolute year {event_year} to relative year {relative_year} "
                    f"(deviation year: {deviation_year})"
                )
                prompt["event_year"] = relative_year

    # Create skeleton record
    db_skeleton = ImagePromptSkeletonDB(
        id=skeleton_id,
        timeline_id=str(timeline_id),
        generation_id=str(generation_id) if generation_id else None,
        status=ImagePromptSkeletonStatus.PENDING.value,
        num_images=num_images,
        focus_areas=focus_areas,
        prompts_json=prompts,
        model_provider=model_provider,
        model_name=model_name,
        created_at=datetime.now(timezone.utc),
    )

    db.add(db_skeleton)
    await db.flush()

    logger.info(
        f"Created image prompt skeleton {skeleton_id} with {len(prompts)} prompts "
        f"for timeline {timeline_id}, generation {generation_id}"
    )

    return await get_image_prompt_skeleton(db, UUID(skeleton_id))


async def get_image_prompt_skeleton(
    db: AsyncSession, skeleton_id: UUID
) -> Optional[dict]:
    """
    Get an image prompt skeleton by ID.

    Args:
        db: Database session
        skeleton_id: Skeleton UUID

    Returns:
        Optional[dict]: Skeleton if found, None otherwise
    """
    result = await db.execute(
        select(ImagePromptSkeletonDB).where(
            ImagePromptSkeletonDB.id == str(skeleton_id)
        )
    )
    skeleton = result.scalar_one_or_none()

    if not skeleton:
        logger.warning(f"Image prompt skeleton not found: {skeleton_id}")
        return None

    return {
        "id": UUID(skeleton.id),
        "timeline_id": UUID(skeleton.timeline_id),
        "generation_id": UUID(skeleton.generation_id) if skeleton.generation_id else None,
        "status": skeleton.status,
        "num_images": skeleton.num_images,
        "focus_areas": skeleton.focus_areas,
        "prompts": skeleton.prompts_json,
        "model_provider": skeleton.model_provider,
        "model_name": skeleton.model_name,
        "created_at": skeleton.created_at,
        "approved_at": skeleton.approved_at,
        "completed_at": skeleton.completed_at,
        "updated_at": skeleton.updated_at,
    }


async def get_image_prompt_skeletons_by_timeline(
    db: AsyncSession, timeline_id: UUID
) -> List[dict]:
    """
    Get all image prompt skeletons for a timeline.

    Args:
        db: Database session
        timeline_id: Timeline UUID

    Returns:
        List[dict]: List of skeletons
    """
    result = await db.execute(
        select(ImagePromptSkeletonDB)
        .where(ImagePromptSkeletonDB.timeline_id == str(timeline_id))
        .order_by(ImagePromptSkeletonDB.created_at.desc())
    )
    skeletons = result.scalars().all()

    responses = []
    for skeleton in skeletons:
        skeleton_data = await get_image_prompt_skeleton(db, UUID(skeleton.id))
        if skeleton_data:
            responses.append(skeleton_data)

    logger.info(f"Retrieved {len(responses)} image prompt skeletons for timeline {timeline_id}")
    return responses


