/**
 * Download markdown content as a file.
 * Uses the browser's download API to save the file locally.
 */
export function downloadMarkdownFile(content: string, filename: string): void {
  try {
    // Create blob with UTF-8 encoding
    const blob = new Blob([content], { type: 'text/markdown;charset=utf-8' });

    // Create temporary download link
    const url = URL.createObjectURL(blob);
    const link = document.createElement('a');
    link.href = url;
    link.download = filename;
    link.style.display = 'none';

    // Trigger download
    document.body.appendChild(link);
    link.click();

    // Cleanup
    setTimeout(() => {
      document.body.removeChild(link);
      URL.revokeObjectURL(url);
    }, 100);
  } catch (error) {
    console.error('Failed to download file:', error);
    throw new Error('Failed to download markdown file. Please try again.');
  }
}

/**
 * Export structured report as markdown file.
 * Combines generation and download in one function.
 */
export function exportStructuredReport(
  generation: Generation,
  timeline: Timeline
): void {
  const markdown = generateStructuredReportMarkdown(generation, timeline);
  const filename = generateFilename(timeline, generation, 'structured');
  downloadMarkdownFile(markdown, filename);
}

/**
 * Export narrative as markdown file.
 * Combines generation and download in one function.
 */
export function exportNarrative(
  generation: Generation,
  timeline: Timeline
): void {
  if (!generation.narrative_prose) {
    throw new Error('This generation does not have narrative prose');
  }

  const markdown = generateNarrativeMarkdown(generation, timeline);
  const filename = generateFilename(timeline, generation, 'narrative');
  downloadMarkdownFile(markdown, filename);
}

