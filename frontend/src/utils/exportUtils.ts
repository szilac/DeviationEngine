/**
 * Export Utilities for Markdown file generation
 *
 * Provides functions to export timeline generations as formatted Markdown files.
 * Supports separate exports for structured reports and narrative prose.
 * Enhanced with translation support for bilingual exports.
 */

import type { Timeline, Generation, SupportedLanguage } from '../types';

/**
 * Sanitize a string for use in filenames.
 * Removes special characters and limits length.
 */
export function sanitizeFilename(str: string): string {
  return str
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '_')  // Replace non-alphanumeric with underscore
    .replace(/^_+|_+$/g, '')      // Remove leading/trailing underscores
    .substring(0, 50);             // Limit length to 50 characters
}

/**
 * Generate a descriptive filename for export.
 *
 * Format: {deviation}__{generation-label}__{years}__{type}.md
 * Example: stock_market_crash_prevention__original__1929-1949__structured.md
 */
export function generateFilename(
  timeline: Timeline,
  generation: Generation,
  type: 'structured' | 'narrative' | `structured_${string}` | `narrative_${string}`
): string {
  const deviationSlug = sanitizeFilename(timeline.root_deviation_description);

  const generationLabel = generation.generation_order === 1
    ? 'original'
    : `extension_${generation.generation_order - 1}`;

  const deviationYear = new Date(timeline.root_deviation_date).getFullYear();
  const startYear = deviationYear + generation.start_year;
  const endYear = deviationYear + generation.end_year;

  return `${deviationSlug}__${generationLabel}__${startYear}-${endYear}__${type}.md`;
}

/**
 * Get generation label for display.
 */
function getGenerationLabel(generation: Generation): string {
  return generation.generation_order === 1
    ? 'Original Timeline'
    : `Extension ${generation.generation_order - 1}`;
}

/**
 * Get absolute years for display.
 */
function getAbsoluteYears(timeline: Timeline, generation: Generation): string {
  const deviationYear = new Date(timeline.root_deviation_date).getFullYear();
  const startYear = deviationYear + generation.start_year;
  const endYear = deviationYear + generation.end_year;
  return `${startYear}-${endYear}`;
}

/**
 * Format provider name for display.
 */
function formatProviderName(provider: string): string {
  const providerMap: Record<string, string> = {
    'google': 'Google Gemini',
    'openrouter': 'OpenRouter',
    'ollama': 'Ollama',
  };
  return providerMap[provider] || provider;
}

/**
 * Generate Markdown content for a structured report.
 */
export function generateStructuredReportMarkdown(
  generation: Generation,
  timeline: Timeline
): string {
  const generationLabel = getGenerationLabel(generation);
  const absoluteYears = getAbsoluteYears(timeline, generation);
  const currentDate = new Date().toLocaleString();

  // Build metadata section
  const metadata: string[] = [
    `# ${timeline.root_deviation_description} - Structured Report`,
    '',
    `**Generation**: ${generationLabel}`,
    `**Time Period**: Years ${generation.start_year}-${generation.end_year} (${generation.period_years} ${generation.period_years === 1 ? 'year' : 'years'})`,
    `**Absolute Years**: ${absoluteYears}`,
    `**Generated**: ${new Date(generation.created_at).toLocaleDateString()}`,
  ];

  // Add model info if available
  if (generation.report_model_provider && generation.report_model_name) {
    metadata.push(`**Model**: ${formatProviderName(generation.report_model_provider)} - ${generation.report_model_name}`);
  }

  metadata.push('', '---', '');

  // Build sections
  const sections: string[] = [
    '## Executive Summary',
    '',
    generation.executive_summary,
    '',
    '---',
    '',
    '## Political Changes',
    '',
    generation.political_changes,
    '',
    '---',
    '',
    '## Conflicts and Wars',
    '',
    generation.conflicts_and_wars,
    '',
    '---',
    '',
    '## Economic Impacts',
    '',
    generation.economic_impacts,
    '',
    '---',
    '',
    '## Social Developments',
    '',
    generation.social_developments,
    '',
    '---',
    '',
    '## Technological Shifts',
    '',
    generation.technological_shifts,
    '',
    '---',
    '',
    '## Key Figures',
    '',
    generation.key_figures,
    '',
    '---',
    '',
    '## Long-term Implications',
    '',
    generation.long_term_implications,
    '',
  ];

  // Build footer
  const footer: string[] = [
    '---',
    '',
    '*Generated by Deviation Engine*',
    `*Exported on: ${currentDate}*`,
  ];

  return [...metadata, ...sections, ...footer].join('\n');
}

